export function filterEmbassies(query) {
  if (!query || !query.trim()) return embassies
  const q = query.toLowerCase().trim()
  return embassies.filter(e =>
    e.countryName.toLowerCase().includes(q) ||
    e.embassyName.toLowerCase().includes(q) ||
    e.address.toLowerCase().includes(q)
  )
}
